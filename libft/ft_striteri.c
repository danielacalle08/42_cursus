#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		i;

	if (!s || !f)
		return ;
	i = 0;
	while (i < (int)ft_strlen(s))
	{
		f(i, &s[i]);
		i++;
	}
}
